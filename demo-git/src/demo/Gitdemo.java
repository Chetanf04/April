package demo;

public class Gitdemo {
    public static void main(String[] args) {
    	System.out.println("Git Demo");
    	for(int i = 1; i <= 2; i++) {
    		
			int j = 2;
			while(j <= 3) {
				int k = 1;
				do {
					System.out.println("i: " + i + " j " + j + " k " + k);
					k++;
				} while(k <= 2);
				j++;
			}
		}
    	}
}
